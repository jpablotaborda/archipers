    public void actualizarjlist(){
        modelo=new DefaultListModel<>();
        for (int i = 0; i < adhile.obtLista().size(); i++) {
            modelo.addElement((i+1)+". "+adhile.obtLista().get(i).recorridoizqder());
        }
        listahilera.setModel(modelo);
    }

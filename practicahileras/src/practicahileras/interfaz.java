    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int indice=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el n√∫mero de la hilera a verificar si es substring: "));
        indice--;
        String ent_pan=JOptionPane.showInputDialog(null, "Ingrese la hilera de la cual se vericar√° si es substring de la original: ");
        Boolean res= adhile.versubstringdepan(indice, ent_pan);
        if(res==true){
            JOptionPane.showMessageDialog(null,"La palabra "+ adhile.devolverstring(indice)+" es substring de "+ ent_pan);
        }
        else{
            JOptionPane.showMessageDialog(null,"La palabra "+ adhile.devolverstring(indice)+" NO es substring de "+ ent_pan);
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int indice1=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el n√∫mero de la primera palabra a verificar anagrama :"));
        indice1--;
         int indice2=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el n√∫mero de la segunda palabra a verificar anagrama :"));
        indice2--;
        Boolean ana=adhile.verianagrama(indice1,indice2);
        if(ana==true){
        
            JOptionPane.showMessageDialog(null, "La hilera "+adhile.devolverstring(indice1) +" es anagrama de "+adhile.devolverstring(indice2));
        }
        else{
            JOptionPane.showMessageDialog(null, "La hilera "+adhile.devolverstring(indice1) +" no es anagrama de "+adhile.devolverstring(indice2));
        }
        actualizarjlist();
    }//GEN-LAST:event_jButton3ActionPerformed

